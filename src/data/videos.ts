export function getVideosByCollection(collectionId: string): Video[] {
  return VIDEOS.filter((video) => video.collection === collectionId)
}

export function getVideoBySlug(slug: string): Video | undefined {
  return VIDEOS.find((video) => video.slug === slug)
}

export function formatBilibiliEmbedUrl(bvid: string): string {
  return `https://player.bilibili.com/player.html?isOutside=true&bvid=${bvid}&high_quality=1&danmaku=0`
}

export function formatBilibiliCoverUrl(bvid: string): string {
  const video = VIDEOS.find((item) => item.bvid === bvid)
  return video?.cover ?? ''
}
